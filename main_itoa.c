char	*ft_itoa(int n)
{
	char *head;
	char *string;
	int x;

	if (n < 0)
		x = (n * -1);
	else
		x = n;
	string = ft_strnew(12);
	if (!string)
		return (NULL);
	head = string;
	while (x > 0)
	{
		printf("x: %d\n", x);
		*string++ = (x % 10) + 48;
		printf("x %% 10 + 48: %c\n", (x % 10) + 48);
		x /= 10;
	}
	if (n < 0)
		*string = '-';
	head = ft_strrev(head);
	return (head);
}

