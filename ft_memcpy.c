#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*dst_h;

	i = -1;
	dst_h = (unsigned char *)dst;
	while (++i < n && (dst || src))
		*(dst_h + i) = *((unsigned char *)src + i);
	return (dst);
}
