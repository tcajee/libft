void	ft_putnbr_fd(int n, int fd)
{

    int  x;

    x = n;
    if (n == 2147483647)
        ft_putstr_fd("2147483647", fd);
    else if (n == -2147483648)
        ft_putstr_fd("-2147483648", fd);
    else if (n < 0)
        ft_putchar_fd('-', fd);
    else if (x > 9)
    {
        ft_putnbr_fd(x / 10, fd);
        ft_putnbr_fd(x % 10, fd);
    }
    else
        ft_putchar_fd(x + 48, fd);
}

